function keyController(event) {
    var x = event.keyCode;
    if (x === 38) {  //UP
        alert ("You pressed the Escape key!");
    }
    if (x === 40) {  //down
        alert ("You pressed the Escape key!");
    }
    if (x === 37) {  //LEFT
        alert ("You pressed the Escape key!");
    }
    if (x === 39) {  //right
        alert ("You pressed the Escape key!");
    }
    if (x === 32) {  //SPACE
        alert ("You pressed the Escape key!");
    }
    if (x === 13) {  //ENTER
        alert ("You pressed the Escape key!");
    }
    if (x === 65) {  //A, numbers go up alphabetically from A
        alert ("You pressed the Escape key!");
    }
    
}